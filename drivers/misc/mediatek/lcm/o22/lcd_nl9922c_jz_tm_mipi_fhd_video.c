static void lcm_setbacklight_cmdq(void *handle, unsigned int level)
{
    LCM_LOGI("%s,nl9922c_jz_tm backlight: level = %d\n", __func__, level);
    if (level == 0) {
        pr_notice("[LCM],nl9922c_jz_tm backlight off, diming disable first\n");
        push_table(handle,
            dimming_close,
            sizeof(dimming_close) / sizeof(struct LCM_setting_table),
            1);
    }
    level = mult_frac(level, BACKLIGHT_MAX_REG_VAL, BACKLIGHT_MAX_APP_VAL);

    level = level << 2;
    bl_level[0].para_list[0] = level >> 8;
    bl_level[0].para_list[1] = level & 0xFF;
    push_table(handle, bl_level, ARRAY_SIZE(bl_level), 1);

    if (backflag == false) {
        MDELAY(40);
        push_table(handle,dimming_close,sizeof(dimming_close) / sizeof(struct LCM_setting_table),1);
        MDELAY(20);
        push_table(handle,dimming_open,sizeof(dimming_open) / sizeof(struct LCM_setting_table),1);
        backflag = true;
    }

}
